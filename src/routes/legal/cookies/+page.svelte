<article class="legal">
  <h1>Política de Cookies</h1>

  <section>
    <h2>¿Qué son las cookies?</h2>
    <p>
      Las cookies son pequeños archivos de texto que los sitios web almacenan en el dispositivo del
      usuario (ordenador, teléfono móvil, tableta) cuando se navega por internet. Sirven para
      recordar información sobre la visita, como preferencias de idioma y otras opciones.
    </p>
  </section>

  <section>
    <h2>¿Qué cookies utiliza este sitio?</h2>
    <p>
      <strong>CitaSegura.es</strong> es un sitio web estático que actualmente <strong>no utiliza cookies</strong>
      propias ni de terceros. No empleamos herramientas de analítica, publicidad ni seguimiento.
    </p>
    <p>
      En caso de que en el futuro se implementasen cookies, esta política se actualizará y se
      solicitará el consentimiento del usuario conforme a la normativa vigente.
    </p>
  </section>

  <section>
    <h2>Cookies de terceros</h2>
    <p>
      Al hacer clic en los enlaces externos que redirigen a las plataformas de citas listadas,
      dichas plataformas pueden instalar sus propias cookies. Recomendamos consultar las políticas
      de cookies de cada sitio externo.
    </p>
  </section>

  <section>
    <h2>Cómo gestionar las cookies</h2>
    <p>
      Puede configurar su navegador para bloquear o eliminar cookies. A continuación puede consultar
      cómo hacerlo en los navegadores más comunes:
    </p>
    <ul>
      <li><strong>Chrome:</strong> Configuración → Privacidad y seguridad → Cookies</li>
      <li><strong>Firefox:</strong> Opciones → Privacidad y seguridad → Cookies</li>
      <li><strong>Safari:</strong> Preferencias → Privacidad → Cookies</li>
      <li><strong>Edge:</strong> Configuración → Cookies y permisos del sitio</li>
    </ul>
  </section>

  <section>
    <h2>Base legal</h2>
    <p>
      Esta política de cookies se rige por la normativa española y europea aplicable:
    </p>
    <ul>
      <li>Ley 34/2002 (LSSI-CE), artículo 22.2.</li>
      <li>Reglamento (UE) 2016/679 (RGPD).</li>
      <li>Directrices de la AEPD sobre el uso de cookies.</li>
    </ul>
  </section>

