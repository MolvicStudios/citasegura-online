  <h1>Política de Privacidad</h1>

  <section>
    <h2>Responsable del tratamiento</h2>
    <p>
      El responsable del tratamiento de los datos personales es el titular del sitio web
      <strong>citasegura.es</strong>.
    </p>
  </section>

  <section>
    <h2>Datos que recopilamos</h2>
    <p>
      Este sitio web no recopila datos personales de los usuarios. No utilizamos formularios de contacto,
      registro de usuarios ni sistemas de seguimiento personalizado. La navegación por el sitio es
      completamente anónima.
    </p>
  </section>

  <section>
    <h2>Cookies</h2>
    <p>
      Para más información sobre el uso de cookies, consulte nuestra
      <a href="/legal/cookies">Política de Cookies</a>.
    </p>
  </section>

  <section>
    <h2>Enlaces a terceros</h2>
    <p>
      Nuestro sitio contiene enlaces a plataformas externas de citas. Cada una de estas plataformas
      tiene su propia política de privacidad. No somos responsables del tratamiento de datos que
      realicen dichas plataformas cuando el usuario acceda a ellas.
    </p>
  </section>

  <section>
    <h2>Derechos del usuario</h2>
    <p>
      De acuerdo con el RGPD y la LOPDGDD, los usuarios tienen derecho a:
    </p>
    <ul>
      <li>Acceso a sus datos personales.</li>
      <li>Rectificación de datos inexactos.</li>
      <li>Supresión de sus datos (derecho al olvido).</li>
      <li>Limitación del tratamiento.</li>
      <li>Portabilidad de los datos.</li>
      <li>Oposición al tratamiento.</li>
    </ul>
    <p>
      Dado que no recopilamos datos personales, estos derechos no son aplicables en el contexto
      de la navegación por este sitio web.
    </p>
  </section>

  <section>
    <h2>Modificaciones</h2>
    <p>
      Nos reservamos el derecho de modificar esta política de privacidad para adaptarla a novedades
      legislativas o jurisprudenciales. Cualquier cambio será publicado en esta página.
    </p>
  </section>

